    'arganzuela': 'precioArganzuela',
    'fuencarral-el-pardo': 'precioFuencarral',
    'moncloa-aravaca': 'precioMoncloa',
    'latina': 'precioLatina',
    'carabanchel': 'precioCarabanchel',
    'usera': 'precioUsera',
    'puente-vallecas': 'precioPuenteVallecas',
    'moratalaz': 'precioMoratalaz',
    'ciudad-lineal': 'precioCiudadLineal',
    'hortaleza': 'precioHortaleza',
    'villaverde': 'precioVillaverde',
    'villa-vallecas': 'precioVillaVallecas',
    'vicalvaro': 'precioVicalvaro',
    'san-blas-canillejas': 'precioSanBlas',
